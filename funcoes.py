

def somar(num1, num2):
    resultado = num1 + num2
    return resultado


def subtracao(num1, num2):
    resultado = num1 - num2
    return resultado


def multiplicacao(num1, num2):
    resultado = num1 * num2
    return resultado


def dividir(num1, num2):
    if num2 == 0:
        return "Erro divisao"
    return num1/num2

